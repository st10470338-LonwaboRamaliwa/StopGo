public class EscapeSequenceApp {

    public static void main(String[] args) {
       System.out.println("                 ------------"); //This is the top line before the stop signs
       System.out.println("   ******** \t\t\t    XXXXXXXX"); // The start of the signs
       System.out.println(" *          *\t\t\t  X          X");
       System.out.println("*   S T O P  * \t\t\t X     GO     X"); // words inside the signs
       System.out.println(" *          * \t\t\t  X          X");
       System.out.println("   ******** \t\t\t    XXXXXXXX");
       System.out.println("      **    \t\t\t       XX"); // \t means Tab
       System.out.println("      **    \t\t\t       XX");
       System.out.println("      **    \t\t\t       XX");
       System.out.println("     ****    \t\t\t      XXXX");
       System.out.println("  **********   \t\t\t   XXXXXXXXXX");
       System.out.println("|----------------------------------------------|");// out means output
       System.out.println("|----------------------------------------------|");/*The is the base of the signs*/
    } /*End of the signs*/
    
}
